	Misi
</h4>

<div class="container">
    <p class="text-left font-weight-bolder">1. Meningkatkan ketersediaan dan keterjangkauan layanan PAUD nonformal dan informal bermutu dalam rangka mewujudkan anak yang cerdas, kreatif, sehat, ceria, berakhlak mulia sesuai dengan karakteristik dan pertumbuhan dan perkembangan anak serta memiliki kesiapan fisik dan mental untuk memasuki pendidikan lebih lanjut.
