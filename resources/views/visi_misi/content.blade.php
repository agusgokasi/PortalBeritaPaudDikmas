    </p>
    <p class="text-left font-weight-bolder">3. Meningkatkan ketersediaan dan keterjangkauan layanan kursus dan pelatihan, dan pemberdayaan perempuan yang bermutu, berkeadilan, berkelanjutan, berdaya saing, berkesetaraan gender untuk pengembangan berkelanjutan, berdaya saing dan relevan dengan kebutuhan individu dan masyarakat.
    </p>
    <p class="text-left font-weight-bolder">4. Melaksanakan penguatan sistem manajemen pendidikan nonformal dan informal meliputi tata kelola, akuntabilitas, dan pencitraan publik dalam rangka penjaminan mutu.
    </p>
